# Issue 049: read_answer_templates namespace should include VerifiedField types
# =============================================================================


@pytest.mark.unit
class TestReaderNamespaceCompleteness:
    """Issue 049: read_answer_templates namespace should include VerifiedField types.

    Templates generated by generate_pydantic_class() include their own import line,
    which works in exec. But the reader's namespace should also provide these types
    so that templates without explicit imports (e.g., hand-written or stripped) work.
    The fix is to use _build_exec_namespace() which includes all primitives.
    """

    def test_reader_namespace_includes_verified_field(self, tmp_path: pytest.TempPathFactory) -> None:
        """Templates using VerifiedField WITHOUT explicit imports should work.

        Currently fails because reader.py's namespace only has
        {BaseAnswer, Field, Literal, List}, missing VerifiedField and primitives.
        """
        # Template with NO import line; relies on exec namespace providing the names
        template_code = """\
class Answer(BaseAnswer):
    target: str = VerifiedField(
        description="The protein target",
        ground_truth="BCL2",
        verify_with=ExactMatch(normalize=["lowercase", "strip"]),
    )
"""
        answers_file = tmp_path / "answers.json"
        answers_file.write_text(json.dumps({"q1_hash": template_code}))

        result = read_answer_templates(answers_file)

        Answer = result["q1_hash"]
        answer = Answer(target="bcl2")
        assert answer.verify() is True

    def test_reader_namespace_includes_boolean_match(self, tmp_path: pytest.TempPathFactory) -> None:
        """BooleanMatch should be available in exec namespace without explicit import."""
        template_code = """\
class Answer(BaseAnswer):
    flag: bool = VerifiedField(
        description="A flag",
        ground_truth=True,
        verify_with=BooleanMatch(),
    )
"""
        answers_file = tmp_path / "answers.json"
        answers_file.write_text(json.dumps({"q1_hash": template_code}))

        result = read_answer_templates(answers_file)

        Answer = result["q1_hash"]
        answer = Answer(flag=True)
        assert answer.verify() is True

    def test_reader_namespace_includes_numeric_tolerance(self, tmp_path: pytest.TempPathFactory) -> None:
        """NumericTolerance should be available in exec namespace without explicit import."""
        template_code = """\
class Answer(BaseAnswer):
    score: float = VerifiedField(
        description="Confidence score",
        ground_truth=0.95,
        verify_with=NumericTolerance(tolerance=0.01, mode="absolute"),
    )
"""
        answers_file = tmp_path / "answers.json"
        answers_file.write_text(json.dumps({"q1_hash": template_code}))

        result = read_answer_templates(answers_file)

        Answer = result["q1_hash"]
        answer = Answer(score=0.955)
        assert answer.verify() is True


