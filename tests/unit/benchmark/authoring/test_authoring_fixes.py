# Issue 051: sys.modules pollution in read_questions_from_file
# =============================================================================


@pytest.mark.unit
class TestSysModulesCleanup:
    """Issue 051: read_questions_from_file should not pollute sys.modules."""

    def test_module_not_left_in_sys_modules(self, tmp_path: pytest.TempPathFactory) -> None:
        """After reading, the dynamically loaded module should be removed from sys.modules."""
        questions_file = tmp_path / "my_questions.py"
        questions_file.write_text(
            "from karenina.schemas.entities import Question\n"
            'all_questions = [Question(question="Q?", raw_answer="A", keywords=[])]\n'
        )

        # Record modules before
        before_keys = set(sys.modules.keys())

        read_questions_from_file(str(questions_file))

        # No new module should remain in sys.modules
        after_keys = set(sys.modules.keys())
        new_keys = after_keys - before_keys
        # Filter to only the dynamically loaded module name
        question_module_keys = {k for k in new_keys if "my_questions" in k}
        assert question_module_keys == set(), f"Module(s) left in sys.modules: {question_module_keys}"

    def test_loading_two_files_with_same_basename(self, tmp_path: pytest.TempPathFactory) -> None:
        """Two files named 'questions.py' in different dirs should both load correctly."""
        dir_a = tmp_path / "a"
        dir_b = tmp_path / "b"
        dir_a.mkdir()
        dir_b.mkdir()

        file_a = dir_a / "questions.py"
        file_a.write_text(
            "from karenina.schemas.entities import Question\n"
            'all_questions = [Question(question="Q-A?", raw_answer="A", keywords=[])]\n'
        )

        file_b = dir_b / "questions.py"
        file_b.write_text(
            "from karenina.schemas.entities import Question\n"
            'all_questions = [Question(question="Q-B?", raw_answer="B", keywords=[])]\n'
        )

        result_a = read_questions_from_file(str(file_a))
        result_b = read_questions_from_file(str(file_b))

        assert result_a[0].question == "Q-A?"
        assert result_b[0].question == "Q-B?"
