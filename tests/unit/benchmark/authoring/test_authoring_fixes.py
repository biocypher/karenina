def _make_spec(attributes: list[dict[str, Any]], descriptions: dict[str, str] | None = None) -> dict[str, Any]:
    """Build a minimal spec dict for generate_pydantic_class."""
    if descriptions is None:
        descriptions = {attr["name"]: f"The {attr['name']} value" for attr in attributes}
    return {"attributes": attributes, "field_descriptions": descriptions}


# =============================================================================
# Issue 048: Missing Literal import in generated code
# =============================================================================


@pytest.mark.unit
class TestLiteralImportGeneration:
    """Issue 048: Generated code with Literal fields should include the Literal import."""

    def test_literal_field_generates_literal_import(self) -> None:
        """Generated code for a Literal field must include 'from typing import Literal'."""
        spec = _make_spec(
            [{"name": "category", "type": "Literal['drug', 'target']", "ground_truth": "drug"}],
            {"category": "The category"},
        )
        code = generate_pydantic_class(spec)

        assert "from typing import Literal" in code

    def test_literal_field_code_is_executable(self) -> None:
        """Generated code with Literal fields must exec successfully in a clean namespace.

        This is the core test: exec() in a clean namespace (no pre-loaded Literal)
        must succeed. Currently fails with NameError because Literal isn't imported.
        """
        spec = _make_spec(
            [{"name": "category", "type": "Literal['drug', 'target']", "ground_truth": "drug"}],
            {"category": "The category"},
        )
        code = generate_pydantic_class(spec)

        # Execute in a clean namespace (no pre-imported Literal)
        exec_ns: dict[str, Any] = {"__builtins__": __builtins__}
        exec(code, exec_ns)  # noqa: S102

        Answer = exec_ns["Answer"]
        answer = Answer(category="drug")
        assert answer.verify() is True

    def test_no_literal_import_when_not_needed(self) -> None:
        """When no Literal fields exist, the typing import should not appear."""
        spec = _make_spec(
            [{"name": "name", "type": "str", "ground_truth": "test"}],
        )
        code = generate_pydantic_class(spec)

        assert "from typing import Literal" not in code

    def test_mixed_literal_and_non_literal_fields(self) -> None:
        """Generated code with mixed field types includes Literal import and works."""
        spec = _make_spec(
            [
                {"name": "name", "type": "str", "ground_truth": "BCL2"},
                {"name": "category", "type": "Literal['drug', 'target']", "ground_truth": "drug"},
                {"name": "count", "type": "int", "ground_truth": 3},
            ],
            {"name": "Entity name", "category": "Category", "count": "Count"},
        )
        code = generate_pydantic_class(spec)

        assert "from typing import Literal" in code

        exec_ns: dict[str, Any] = {"__builtins__": __builtins__}
        exec(code, exec_ns)  # noqa: S102

        Answer = exec_ns["Answer"]
        answer = Answer(name="bcl2", category="drug", count=3)
        assert answer.verify() is True


